#include<iostream>
using namespace std;

int main() {
    wchar_t a = 'A';
    cout << "a = " << a << endl;
    
    wchar_t b = L'A';
    cout << "b = " << b << endl;
    
    wchar_t c = 'AB';
    cout << "a = " << c << endl;
    
    wchar_t d = L'AB';
    cout << "d = " << d << endl;
    
    
    return 0;
}