#include <iostream>
using namespace std;

int main() {
    int num1 = 7;
    int num2 = 3;

    // Arithmetic Addition
    cout << num1 << " + " << num2 << " = " << num1 + num2 << endl;

    // Arithmetic Subtraction
    cout << num1 << " - " << num2 << " = " << num1 - num2 << endl;

    // Arithmetic Multiplication
    cout << num1 << " * " << num2 << " = " << num1 * num2 << endl;

    // Arithmetic Division
    cout << num1 << " / " << num2 << " = " << num1 / num2 << endl;

    // Arithmetic Modulus
    cout << num1 << " % " << num2 << " = " << num1 % num2 << endl;

    return 0;
}
